                    rs=stmt.executeQuery();
                    if(rs.next())
                    {    
                        boolean flag=false;
                        if(rs.getString(1).equalsIgnoreCase(username.getText()) && rs.getString(2).equals(password.getText()))
                                  flag=true;
                        System.out.print(flag);
                        if(flag)
                            new EntryForm().setVisible(true);  
                        else
                            JOptionPane.showMessageDialog(this,"Your username or password is wrong!");
                     }
                    else
                        JOptionPane.showMessageDialog(this,"Records not found!");
